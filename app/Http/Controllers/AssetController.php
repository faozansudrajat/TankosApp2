     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $pr = Assets::find($id);
        $pr->harga = $request->input('harga');
        $pr->stock = $request->input('stock');
        $pr->save();
        return redirect()->route('asset.index');
